    public void pontua(){
        sort();
        
        String pnt = "<html><span style='color:red;'>Recordes:</span> <br>";
        for (int i = 0; i <this.recordes.size(); i++) {
            pnt +=  (recordes.get(i).getNome()+" - "+recordes.get(i).getValor()+" pontos <br>");
        }
        pnt += "</html>";
        recordeslbl.setText(pnt);
    }
