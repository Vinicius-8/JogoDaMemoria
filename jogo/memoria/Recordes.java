    public void sort(){
        for (int i = 0; i < recordes.size(); i++) {
            for (int j = i; j < recordes.size()-1; j++) {
                if (recordes.get(j).getValor()<recordes.get(j+1).getValor()) {
                    Pontos p = recordes.get(j);
                    recordes.set(j, recordes.get(j+1));
                    recordes.set(j+1, p);
                }
            }
        }
    }
